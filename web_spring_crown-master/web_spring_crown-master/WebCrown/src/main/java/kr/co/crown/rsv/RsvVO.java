package kr.co.crown.rsv;

public class RsvVO {

	private int rsv_code;
	private int guest_code;
	private int member_code;
	private String room_num;
	public int getRsv_code() {
		return rsv_code;
	}
	public void setRsv_code(int rsv_code) {
		this.rsv_code = rsv_code;
	}
	public int getGuest_code() {
		return guest_code;
	}
	public void setGuest_code(int guest_code) {
		this.guest_code = guest_code;
	}
	public int getMember_code() {
		return member_code;
	}
	public void setMember_code(int member_code) {
		this.member_code = member_code;
	}
	public String getRoom_num() {
		return room_num;
	}
	public void setRoom_num(String room_num) {
		this.room_num = room_num;
	}
	
	
	
}
